    public JSONObject create(Order order) {							
        /** 記錄實際執行之SQL指令 */
        String exexcute_sql = "";
        long id = -1;
        JSONArray oma = new JSONArray();
        
        try {
            /** 取得資料庫之連線 */
            conn = DBMgr.getConnection();
            /** SQL指令 */
            String sql = "INSERT INTO `final_pj`.`Order`(`member_id`, `movie_id`, `room_id`, `order_date`, `order_total_price`, `order_time_of_day`, `order_datetime`, `order_status`)"
                    + " VALUES(?, ?, ?, ?, ?, ?, ?, ?)";  																
            
			 
            /** 取得所需之參數 */
            int member_id = order.getOrderMemberId();
            int movie_id = order.getOrderMovieId();
            int room_id = order.getOrderRoomId();
            String date = order.getOrderDate();
			int total_price = order.getOrderTotalPrice();
            int time_of_day = order.getOrderTimeOfDay();
			Timestamp datetime = order.getOrderDatetime();
            
            /** 將參數回填至SQL指令當中 */
            pres = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            pres.setInt(1, member_id);
            pres.setInt(2, movie_id);
            pres.setInt(3, room_id);
            pres.setString(4, date);
            pres.setInt(5, total_price);
            pres.setInt(6, time_of_day);
			pres.setTimestamp(7, datetime);
            pres.setInt(8, 0);
            
            /** 執行新增之SQL指令並記錄影響之行數 */
            pres.executeUpdate();
            
            /** 紀錄真實執行的SQL指令，並印出 **/
            exexcute_sql = pres.toString();
            System.out.println(exexcute_sql);
            
            ResultSet rs = pres.getGeneratedKeys();

            if (rs.next()) {
                id = rs.getLong(1);
                ArrayList<OrderMeal> omd = order.getOrderMealList();
                oma = omh.createByList(id, omd);
            }
        } catch (SQLException e) {
            /** 印出JDBC SQL指令錯誤 **/
            System.err.format("SQL State: %s\n%s\n%s", e.getErrorCode(), e.getSQLState(), e.getMessage());
        } catch (Exception e) {
            /** 若錯誤則印出錯誤訊息 */
            e.printStackTrace();
        } finally {
            /** 關閉連線並釋放所有資料庫相關之資源 **/
            DBMgr.close(pres, conn);
        }

        /** 將SQL指令、花費時間與影響行數，封裝成JSONObject回傳 */
        JSONObject response = new JSONObject();
        response.put("order_id", id);
        response.put("order_meal_id", oma);							

        return response;
    }
    
